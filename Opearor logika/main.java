import javax.print.event.PrintEvent;

class main {

	public static void main(String[] args) {

		// operator logika --> Operasi yang bisa dilakukan pada tipe data boolean

		boolean ucup, renal;
		ucup = true;
		renal = false;

		System.out.println(ucup && renal);
		System.out.println(ucup || renal);
		System.out.println(ucup ^ renal);
		System.out.println(!(ucup ^ renal));

	}
}